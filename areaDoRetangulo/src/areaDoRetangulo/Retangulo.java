package areaDoRetangulo;

public class Retangulo {

	public static void main(String[] args) {
		
		System.out.println("Olá Mundo!");

		int b = 5;
		int h = 3;
	
		int a = b * h;
		
		System.out.println("");
		System.out.print("**** AREA DO RETANGULO ****");
		System.out.println("");
		System.out.println("");
		System.out.print("Base: ");
		System.out.println(b);
		System.out.print("Altura: ");
		System.out.print(h);
		System.out.println("");
		System.out.println("");
		System.out.println("-----------------------");
		System.out.println("");
		System.out.print("Área do retangulo: ");
		System.out.print(a);
	}

}
